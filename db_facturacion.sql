CREATE TABLE FACTURAS
(
NRO_FACTURA INT IDENTITY (1,1),
FECHA DATETIME,
ID_FORMAPAGO INT,
CLIENTE VARCHAR (50)

CONSTRAINT PK_FACTURA PRIMARY KEY (NRO_FACTURA),
CONSTRAINT FK_FACTURA_FORMAS_PAGO FOREIGN KEY (ID_FORMAPAGO)
REFERENCES FORMAS_PAGO (ID_FORMAPAGO)
)

