REFERENCES FACTURAS (NRO_FACTURA),
CONSTRAINT FK_DETALLES_FACTURA_ARTICULOS FOREIGN KEY (ID_ARTICULO)
REFERENCES ARTICULOS (ID_ARTICULO)
)

