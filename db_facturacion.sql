(
ID_ARTICULO INT IDENTITY (1,1),
DESCRIPCION VARCHAR (50),
PRE_UNITARIO DECIMAL (10,2)

CONSTRAINT PK_ARTICULOS PRIMARY KEY (ID_ARTICULO)
